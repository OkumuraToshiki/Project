#include "TitleScene.h"
#include "Camera.h"
#include "input.h"
class GameScene;
class InputDebugScene;
extern CCamera g_Camera;
/*===========================================================================
  コンストラクタ
===========================================================================*/
TitleScene::TitleScene()
{
	CCamera::Set(&g_Camera);
}
/*===========================================================================
  デストラクタ
===========================================================================*/
TitleScene::~TitleScene()
{
}
/*===========================================================================
  初期処理
===========================================================================*/
void TitleScene::Init()
{
}
/*===========================================================================
  終了処理
===========================================================================*/
void TitleScene::Uninit()
{
}
/*===========================================================================
  更新処理
===========================================================================*/
BaseScene* TitleScene::Update()
{
	if (GetKeyTrigger(VK_RETURN))
	{
		return makeScene<GameScene>();
	}
#ifdef _DEBUG
	if (GetKeyPress(VK_D) && GetKeyTrigger(VK_C))
	{
		return makeScene<InputDebugScene>();
	}
#endif
	return this;
}
/*===========================================================================
  描画処理
===========================================================================*/
void TitleScene::Draw() const
{
}
/*===========================================================================
  シーン名取得
===========================================================================*/
std::string TitleScene::GetSceneName()
{
	return "TitleScene";
}
/*===========================================================================
  タイトルシーン生成
===========================================================================*/
template<>
BaseScene *BaseScene::makeScene<TitleScene>() 
{
	return new TitleScene();
}